# Задача 14: Требуется вывести все целые степени двойки 
# (т.е. числа вида 2^k), не превосходящие числа N.

import math
N = int(input('Enter limit: '))
power = 0
while (p := math.pow(2, power)) <= N:
    print(f'2 ^ {power} = {int(p)}')
    power += 1

